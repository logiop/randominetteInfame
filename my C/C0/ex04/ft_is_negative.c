#include <unistd.h>

void	ft_is_negative(int n)
{
	char	p;
	char	x;

	x = 'N';
	p = 'P';
	if (n >= 0)
	{
		write(1, &p, 1);
	}
	else
	{
		write(1, &x, 1);
	}
}
