#include <unistd.h>

void	numb(int n, char*s)
{
	s[0] = n / 10 + 48;
	s[1] = n % 10 + 48;
}

void	ft_print_comb2(void)
{
	int		i;
	int		j;
	char	n[2];

	i = 0;
	while (i <= 98)
	{
		j = i + 1;
		while (j <= 99)
		{
			numb(i, n);
			write(1, &n, 2);
			write(1, " ", 1);
			numb(j, n);
			write(1, &n, 2);
			if (i != 98)
			{
				write(1, ",  ", 2);
			}
			j++;
		}
		i++;
	}
}
